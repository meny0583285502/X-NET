# PHASE 1 - dns_server.ps1
# ==================================================
$dnsLines = [System.Collections.Generic.List[string]]::new()
$dnsLines.Add('# X-NET DNS + HTTP Server v14.0')
$dnsLines.Add('$DIR = "C:\XNET"')
$dnsLines.Add('$LOG = "$DIR\dns_log.txt"')
$dnsLines.Add('$DASHBOARD_URL = "https://meny0583285502.github.io/X-NET/"')
$dnsLines.Add('function Log($m) {')
$dnsLines.Add('    "$(Get-Date -Format ''HH:mm:ss'') $m" | Out-File $LOG -Append -Encoding UTF8')
$dnsLines.Add('    # Keep log under 200 lines')
$dnsLines.Add('    if ((Get-Item $LOG -ErrorAction SilentlyContinue).Length -gt 50000) {')
$dnsLines.Add('        $lines = Get-Content $LOG -ErrorAction SilentlyContinue | Select-Object -Last 100')
$dnsLines.Add('        $lines | Out-File $LOG -Encoding UTF8 -Force')
$dnsLines.Add('    }')
$dnsLines.Add('}')
$dnsLines.Add('')
$dnsLines.Add('function Get-Whitelist {')
$dnsLines.Add('    $wl = @{}')
$dnsLines.Add('    $f = "$DIR\whitelist.txt"')
$dnsLines.Add('    if (Test-Path $f) {')
$dnsLines.Add('        Get-Content $f | ForEach-Object {')
$dnsLines.Add('            $d = $_.Trim().ToLower()')
$dnsLines.Add('            if ($d -and -not $d.StartsWith("#")) { $wl[$d] = $true }')
$dnsLines.Add('        }')
$dnsLines.Add('    }')
$dnsLines.Add('    return $wl')
$dnsLines.Add('}')
$dnsLines.Add('')
$dnsLines.Add('function Get-Domain($data) {')
$dnsLines.Add('    try {')
$dnsLines.Add('        $i = 12; $labels = @()')
$dnsLines.Add('        while ($i -lt $data.Length) {')
$dnsLines.Add('            $len = $data[$i]')
$dnsLines.Add('            if ($len -eq 0) { break }')
$dnsLines.Add('            $labels += [System.Text.Encoding]::ASCII.GetString($data[($i+1)..($i+$len)])')
$dnsLines.Add('            $i += $len + 1')
$dnsLines.Add('        }')
$dnsLines.Add('        return ($labels -join ".").ToLower()')
$dnsLines.Add('    } catch { return "" }')
$dnsLines.Add('}')
$dnsLines.Add('')
$dnsLines.Add('function Forward-DNS($data) {')
$dnsLines.Add('    try {')
$dnsLines.Add('        $udp = New-Object System.Net.Sockets.UdpClient')
$dnsLines.Add('        $udp.Client.ReceiveTimeout = 2000')
$dnsLines.Add('        $ep = [System.Net.IPEndPoint]::new([Net.IPAddress]::Parse("8.8.8.8"), 53)')
$dnsLines.Add('        [void]$udp.Send($data, $data.Length, $ep)')
$dnsLines.Add('        $rep = [System.Net.IPEndPoint]::new([Net.IPAddress]::Any, 0)')
$dnsLines.Add('        $r = $udp.Receive([ref]$rep)')
$dnsLines.Add('        $udp.Close(); return $r')
$dnsLines.Add('    } catch { return $null }')
$dnsLines.Add('}')
$dnsLines.Add('')
$dnsLines.Add('function Build-NXDOMAIN($query) {')
$dnsLines.Add('    $r = $query[0..1] + [byte[]](0x81,0x83) + [byte[]](0x00,0x01,0x00,0x00,0x00,0x00,0x00,0x00) + $query[12..($query.Length-1)]')
$dnsLines.Add('    return $r')
$dnsLines.Add('}')
$dnsLines.Add('')
$dnsLines.Add('# Start HTTP server on 5.5.0.2:80 for dashboard redirect')
$dnsLines.Add('$http = $null')
$dnsLines.Add('try {')
$dnsLines.Add('    Add-Type -AssemblyName System.Net.HttpListener -ErrorAction SilentlyContinue 2>$null')
$dnsLines.Add('    $http = New-Object System.Net.HttpListener')
$dnsLines.Add('    $http.Prefixes.Add("http://5.5.0.2:80/")')
$dnsLines.Add('    $http.Prefixes.Add("http://127.0.0.1:8080/")')
$dnsLines.Add('    $http.Start()')
$dnsLines.Add('    Log "HTTP server started on 5.5.0.2:80"')
$dnsLines.Add('} catch {')
$dnsLines.Add('    Log "HTTP server FAIL (need admin or netsh): $_"')
$dnsLines.Add('    $http = $null')
$dnsLines.Add('}')
$dnsLines.Add('')
$dnsLines.Add('# Reserve 5.5.0.2 on loopback if not already done')
$dnsLines.Add('$lo = Get-NetIPAddress -IPAddress "5.5.0.2" -ErrorAction SilentlyContinue')
$dnsLines.Add('if (-not $lo) {')
$dnsLines.Add('    netsh interface ip add address "Loopback Pseudo-Interface 1" 5.5.0.2 255.255.255.255 2>$null | Out-Null')
$dnsLines.Add('    if ($LASTEXITCODE -ne 0) {')
$dnsLines.Add('        netsh interface ip add address "Loopback" 5.5.0.2 255.255.255.255 2>$null | Out-Null')
$dnsLines.Add('    }')
$dnsLines.Add('    Log "Added 5.5.0.2 to loopback"')
$dnsLines.Add('}')
$dnsLines.Add('')
$dnsLines.Add('# DNS socket')
$dnsLines.Add('$socket = New-Object System.Net.Sockets.UdpClient 53')
$dnsLines.Add('$socket.Client.ReceiveTimeout = 500')
$dnsLines.Add('Log "DNS server started"')
$dnsLines.Add('# Trim log on startup if too large')
$dnsLines.Add('if ((Get-Item $LOG -ErrorAction SilentlyContinue).Length -gt 102400) {')
$dnsLines.Add('    $keep = Get-Content $LOG -ErrorAction SilentlyContinue | Select-Object -Last 50')
$dnsLines.Add('    $keep | Out-File $LOG -Encoding UTF8 -Force')
$dnsLines.Add('}')
$dnsLines.Add('$wl = Get-Whitelist')
$dnsLines.Add('$wlTime = Get-Date')
$dnsLines.Add('')
$dnsLines.Add('while ($true) {')
$dnsLines.Add('    # Reload whitelist every 60s')
$dnsLines.Add('    if ((Get-Date) - $wlTime -gt [TimeSpan]::FromSeconds(60)) {')
$dnsLines.Add('        $wl = Get-Whitelist; $wlTime = Get-Date')
$dnsLines.Add('    }')
$dnsLines.Add('    # Handle HTTP requests (non-blocking)')
$dnsLines.Add('    if ($http -and $http.IsListening) {')
$dnsLines.Add('        try {')
$dnsLines.Add('            $ctx = $null')
$dnsLines.Add('            $task = $http.GetContextAsync()')
$dnsLines.Add('            if ($task.Wait(10)) {')
$dnsLines.Add('                $ctx = $task.Result')
$dnsLines.Add('                $uf = (Get-Content "$DIR\user.txt" -First 1 -ErrorAction SilentlyContinue).Trim()')
$dnsLines.Add('                $redir = $DASHBOARD_URL + "?user=" + $uf')
$dnsLines.Add('                $ctx.Response.Redirect($redir)')
$dnsLines.Add('                $ctx.Response.Close()')
$dnsLines.Add('            }')
$dnsLines.Add('        } catch {}')
$dnsLines.Add('    }')
$dnsLines.Add('    # Handle DNS')
$dnsLines.Add('    try {')
$dnsLines.Add('        $ep = [System.Net.IPEndPoint]::new([Net.IPAddress]::Any, 0)')
$dnsLines.Add('        $data = $socket.Receive([ref]$ep)')
$dnsLines.Add('        $domain = Get-Domain $data')
$dnsLines.Add('        if (-not $domain) { continue }')
$dnsLines.Add('        $allowed = $false')
$dnsLines.Add('        $parts = $domain -split "\."')
$dnsLines.Add('        for ($j = 0; $j -lt ($parts.Count - 1); $j++) {')
$dnsLines.Add('            $sub = ($parts[$j..($parts.Count-1)]) -join "."')
$dnsLines.Add('            if ($wl.ContainsKey($sub)) { $allowed = $true; break }')
$dnsLines.Add('        }')
$dnsLines.Add('        if ($allowed) {')
$dnsLines.Add('            $resp = Forward-DNS $data')
$dnsLines.Add('            if (-not $resp) { $resp = Build-NXDOMAIN $data }')
$dnsLines.Add('        } else {')
$dnsLines.Add('            $resp = Build-NXDOMAIN $data')
$dnsLines.Add('        }')
$dnsLines.Add('        [void]$socket.Send($resp, $resp.Length, $ep)')
$dnsLines.Add('    } catch [System.Net.Sockets.SocketException] {}')
$dnsLines.Add('    catch { Log "Error: $_" }')
$dnsLines.Add('}')

[IO.File]::WriteAllLines("$DIR\dns_server.ps1", $dnsLines, [Text.Encoding]::UTF8)
if (-not $Silent) { Write-Host "[+] dns_server.ps1 written." -ForegroundColor Green }

